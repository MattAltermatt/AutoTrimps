// Regenerate the src-bundle byte golden — and RECORD WHY.
//
// #91: this used to be a one-liner that silently overwrote the baseline, and the net's only defence
// against laundering a regression was a code comment asking the developer to eyeball the diff. That is
// not a defence. The documented sequence on any fix wave was:
//
//     change src/  →  parity goes red  →  run this script  →  green  →  deploy
//
// which is exactly the sequence that let an injected `jobs.ts` regression ship to `dist/` GREEN during
// #67's end-to-end experiment. **This command was the thing that silenced the only red signal.**
//
// So regenerating is now an explicit, reviewable act — the way an oracle re-pin is. It demands a reason,
// writes that reason plus the golden's hash into a committed manifest, and `tests/src-bundle-parity.test.ts`
// refuses any golden whose hash does not match its manifest. You cannot regenerate without saying why, and
// the why lands in the diff where a reviewer actually sees it.
//
//     node scripts/regen-src-golden.mjs --reason "#88: initialize MODULES.jobs.customRatio"
//
// This cannot stop a determined liar — nothing can. It converts a *silent side effect of a build command*
// into an *explicit, attributable claim*. That is the whole difference between a hatch and a door.
import { writeFile } from 'node:fs/promises'
import { resolve, dirname } from 'node:path'
import { fileURLToPath } from 'node:url'
import { createHash } from 'node:crypto'
import { bundleSrc } from './build-userscript.mjs'

const ROOT = resolve(dirname(fileURLToPath(import.meta.url)), '..')
const GOLDEN = resolve(ROOT, 'tests/fixtures/src-bundle.golden.js')
const MANIFEST = resolve(ROOT, 'tests/fixtures/src-bundle.manifest.json')

const argv = process.argv.slice(2)
const at = argv.indexOf('--reason')
const reason = (at >= 0 ? (argv[at + 1] ?? '') : '').trim()

if (reason.length < 15) {
  console.error(
    [
      '',
      'REFUSING to regenerate the byte golden without a reason (#91).',
      '',
      '  node scripts/regen-src-golden.mjs --reason "<why the emitted bundle legitimately changed>"',
      '',
      'The golden is the only check that notices an UNINTENDED change to the emitted JS. Overwriting it is',
      'how a regression gets laundered — it is literally what silenced the injected-bug experiment in #67.',
      '',
      'If you cannot state why the emit moved, THAT IS THE FINDING. Do not regenerate. Go read the diff.',
      '',
    ].join('\n'),
  )
  process.exit(1)
}

const out = await bundleSrc()
const sha256 = createHash('sha256').update(out).digest('hex')

await writeFile(GOLDEN, out)
await writeFile(
  MANIFEST,
  JSON.stringify(
    {
      _comment:
        'Written by scripts/regen-src-golden.mjs (#91). `sha256` must match tests/fixtures/src-bundle.golden.js — ' +
        'tests/src-bundle-parity.test.ts enforces it, so a hand-edited golden, or one regenerated by any means ' +
        'that skips the reason prompt, fails the gate.',
      sha256,
      bytes: out.length,
      reason,
    },
    null,
    2,
  ) + '\n',
)

console.log(`wrote tests/fixtures/src-bundle.golden.js (${out.length} bytes)`)
console.log(`wrote tests/fixtures/src-bundle.manifest.json`)
console.log(`  sha256: ${sha256.slice(0, 16)}…`)
console.log(`  reason: ${reason}`)
